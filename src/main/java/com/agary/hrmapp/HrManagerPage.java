    private void btnAddEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmployeeActionPerformed
        // TODO add your handling code here:
        boolean valid = true;
        if (txtEpfNumber.getText().isEmpty() || txtEmployeeName.getText().isEmpty() || cmbDepartment.getSelectedItem().toString().isEmpty() || cmbDesignation.getSelectedItem().toString().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Field can't be black", "Error in Adding Employee", JOptionPane.ERROR_MESSAGE);
            valid = false;
        }else{
        if (valid && checkAvailabilityEmp() == false) {
            String epfNo = txtEpfNumber.getText();
            int epfNoinInt = Integer.parseInt(epfNo);
            String epfName = txtEmployeeName.getText();
            String depName = cmbDepartment.getSelectedItem().toString();
            String desigName = cmbDesignation.getSelectedItem().toString();

            ArrayList<Employee> employees = hrMnr.addEmployee(epfNoinInt, epfName, depName, desigName);
            Employee.writeFile(employees, "/Users/arthathagary/Documents/ICBT Docs/Assesments/HrmAppTxtFiles/employeesDetails.txt");
            JOptionPane.showMessageDialog(rootPane, "Succesfully wrote employee details to the file");
        } else {
            JOptionPane.showMessageDialog(rootPane, "EPF Number already available");
        }
        }
    }//GEN-LAST:event_btnAddEmployeeActionPerformed
