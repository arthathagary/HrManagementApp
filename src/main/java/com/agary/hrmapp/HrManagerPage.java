    private void initiateEmployees() {
        try {
            try (FileReader fileReader = new FileReader("/Users/arthathagary/Documents/ICBT Docs/Assesments/HrmAppTxtFiles/departmentDetails.txt")) {
                BufferedReader bufferedReader = new BufferedReader(fileReader);
                FileReader fileReader2 = new FileReader("/Users/arthathagary/Documents/ICBT Docs/Assesments/HrmAppTxtFiles/designationDetails.txt");
                BufferedReader bufferedReader2 = new BufferedReader(fileReader2);
                String line, line2, deptName, desigName;
                while ((line = bufferedReader.readLine()) != null) {
                    deptName = line.split(" ")[1];
                    cmbDepartment.addItem(deptName);
                    cmbDeleteDep.addItem(deptName);
                }
                bufferedReader.close();
                while ((line2 = bufferedReader2.readLine()) != null) {
                    desigName = line2.split(" ")[1];
                    cmbDesignation.addItem(desigName);
                    cmbDeleteDesignation.addItem(desigName);
                }
                bufferedReader2.close();
                fileReader2.close();
            }
        } catch (FileNotFoundException ex) {
            System.err.println(ex);
        } catch (IOException ex) {
            System.err.println(ex);
        }

    }

